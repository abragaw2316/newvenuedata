// Generate licensesignal/lib/county-stats.ts — REAL per-county AND per-city
// aggregates computed from the full API dataset (data/licenses.json), used to make
// the programmatic /coverage/[county] and /coverage/[county]/[type] and
// /coverage/[county]/city/[city] pages data-backed instead of estimated.
//
//   node src/build-coverage-stats.mjs   (run after build-full-data.mjs)
import { readFile, writeFile } from 'node:fs/promises'
import { fileURLToPath } from 'node:url'

const LICENSES = fileURLToPath(new URL('../../licensesignal/data/licenses.json', import.meta.url))
const OUT = fileURLToPath(new URL('../../licensesignal/lib/county-stats.ts', import.meta.url))
const asOf = '2026-06-15'
// Only emit a city page when the dataset holds at least this many records for the
// city — keeps city pages genuinely data-rich (no thin one-record pages).
const CITY_MIN = 25

// Match the slug convention in lib/fl-counties.ts (e.g. "St. Johns" → "st-johns",
// "Miami-Dade" → "miami-dade", "DeSoto" → "desoto").
const slugify = (name) =>
  String(name || '')
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-|-$/g, '')

const records = JSON.parse(await readFile(LICENSES, 'utf8'))

const byCounty = new Map()
for (const r of records) {
  const county = r.address?.county
  if (!county) continue
  const slug = slugify(county)
  if (!slug) continue
  let c = byCounty.get(slug)
  if (!c) {
    c = { total: 0, newFilings: 0, byType: {}, cities: new Map() }
    byCounty.set(slug, c)
  }
  c.total++
  if (r.eventType === 'new_filing') c.newFilings++
  c.byType[r.licenseType] = (c.byType[r.licenseType] || 0) + 1
  const city = r.address?.city
  if (city) {
    let cd = c.cities.get(city)
    if (!cd) {
      cd = { city, total: 0, byType: {} }
      c.cities.set(city, cd)
    }
    cd.total++
    cd.byType[r.licenseType] = (cd.byType[r.licenseType] || 0) + 1
  }
}

const stats = {}
const cityStats = {}
let cityPageCount = 0
for (const [slug, c] of byCounty) {
  const cityList = [...c.cities.values()].sort((a, b) => b.total - a.total)
  stats[slug] = {
    total: c.total,
    newFilings: c.newFilings,
    byType: c.byType,
    topCities: cityList.slice(0, 6).map((cd) => ({ city: cd.city, count: cd.total })),
  }
  const bigCities = cityList
    .filter((cd) => cd.total >= CITY_MIN && slugify(cd.city))
    .map((cd) => ({ citySlug: slugify(cd.city), city: cd.city, total: cd.total, byType: cd.byType }))
  if (bigCities.length) {
    cityStats[slug] = bigCities
    cityPageCount += bigCities.length
  }
}

const totalLicenses = records.length
const countiesWithData = Object.keys(stats).length

const banner = `// AUTO-GENERATED by data-pipeline/src/build-coverage-stats.mjs from the full
// New Venue Data dataset (data/licenses.json). Real per-county + per-city counts that
// power the programmatic /coverage pages. Do not hand-edit — re-run the pipeline.
// Sourced ${asOf}. Compliance: Ch. 119 public records; see data-pipeline/SOURCES.md.
`
const ts =
  banner +
  `export interface CountyStat {\n` +
  `  /** Total licenses tracked in this county in the current dataset. */\n` +
  `  total: number\n` +
  `  /** Records flagged as new filings. */\n` +
  `  newFilings: number\n` +
  `  /** Count by license-type code (COP, APS, BEV, FOOD_SERVICE, SEATING, MOBILE_FOOD). */\n` +
  `  byType: Record<string, number>\n` +
  `  /** Real top cities by record count. */\n` +
  `  topCities: { city: string; count: number }[]\n` +
  `}\n\n` +
  `export interface CityStat {\n` +
  `  citySlug: string\n` +
  `  city: string\n` +
  `  total: number\n` +
  `  byType: Record<string, number>\n` +
  `}\n\n` +
  `export const COVERAGE_DATA_AS_OF = '${asOf}'\n` +
  `export const COVERAGE_TOTAL_LICENSES = ${totalLicenses}\n` +
  `export const COVERAGE_COUNTIES_WITH_DATA = ${countiesWithData}\n\n` +
  `export const COUNTY_STATS: Record<string, CountyStat> = ${JSON.stringify(stats, null, 2)}\n\n` +
  `/** Cities (per county slug) with >= ${CITY_MIN} records — the city pages that exist. */\n` +
  `export const CITY_STATS: Record<string, CityStat[]> = ${JSON.stringify(cityStats, null, 2)}\n\n` +
  `export function getCountyStat(slug: string): CountyStat | undefined {\n` +
  `  return COUNTY_STATS[slug]\n` +
  `}\n\n` +
  `export function getCityStat(countySlug: string, citySlug: string): CityStat | undefined {\n` +
  `  return CITY_STATS[countySlug]?.find((c) => c.citySlug === citySlug)\n` +
  `}\n`

await writeFile(OUT, ts, 'utf8')
console.log(`✓ wrote county stats for ${countiesWithData} counties → licensesignal/lib/county-stats.ts`)
console.log(`  city pages (>= ${CITY_MIN} records): ${cityPageCount}`)
const top = Object.entries(stats).sort((a, b) => b[1].total - a[1].total).slice(0, 5)
console.log(`  top counties: ${top.map(([s, v]) => `${s} ${v.total}`).join(', ')}`)
